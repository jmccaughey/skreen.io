package com.deskblast.scraper;

public class SubtileInfo {

	private String hash;
	private int x;
	private int y;
	
	public SubtileInfo(String hash, int x, int y) {
		super();
		this.hash = hash;
		this.x = x;
		this.y = y;
	}

	public String getHash() {
		return hash;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}
	

	
}
